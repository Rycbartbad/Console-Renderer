Layer2D::Layer2D(int width, int height, int z_order)
    : m_width(width), m_height(height), m_z(z_order)
    , m_cells(static_cast<size_t>(width) * height) { clear(); }

void Layer2D::clear(Vec3 bg) {
    Cell empty; empty.bg = bg; empty.fg = Vec3(255,255,255);
    empty.ch = ' '; empty.transparent = false; empty.has_text = false;
    std::fill(m_cells.begin(), m_cells.end(), empty);
}

void Layer2D::set_cell(int x, int y, char ch, Vec3 fg, Vec3 bg) {
    if (x < 0 || x >= m_width || y < 0 || y >= m_height) return;
    auto& c = m_cells[x + y * m_width];
    c.bg = bg; c.fg = fg; c.ch = ch; c.transparent = false; c.has_text = (ch != ' ');
}

void Layer2D::fill_rect(int x, int y, int w, int h, Vec3 bg) {
    int x0 = std::max(0, x), y0 = std::max(0, y);
    int x1 = std::min(m_width, x + w), y1 = std::min(m_height, y + h);
    for (int row = y0; row < y1; row++)
        for (int col = x0; col < x1; col++)
            m_cells[col + row * m_width].bg = bg,
            m_cells[col + row * m_width].transparent = false;
}

void Layer2D::draw_border(int x, int y, int w, int h, Vec3 color) {
    for (int i = 1; i < w - 1; i++) {
        set_cell(x + i, y, '\xC4', color, color);
        set_cell(x + i, y + h - 1, '\xC4', color, color);
    }
    for (int i = 1; i < h - 1; i++) {
        set_cell(x, y + i, '\xB3', color, color);
        set_cell(x + w - 1, y + i, '\xB3', color, color);
    }
    set_cell(x, y, '\xDA', color, color);
    set_cell(x + w - 1, y, '\xBF', color, color);
    set_cell(x, y + h - 1, '\xC0', color, color);
    set_cell(x + w - 1, y + h - 1, '\xD9', color, color);
}

void Layer2D::draw_text(int x, int y, const std::string& text, Vec3 fg, Vec3 bg, int scale) {
    if (scale < 1) scale = 1;
    for (size_t ci = 0; ci < text.size(); ci++) {
        unsigned char ch = (unsigned char)text[ci];
        if (ch < 32 || ch > 126) ch = '?';
        int cx = x + (int)ci * 8 * scale;
        for (int row = 0; row < 8; row++) {
            uint8_t bits = FONT8x8[(ch - 32) * 8 + row];
            for (int col = 0; col < 8; col++) {
                if (!(bits & (0x80 >> col))) continue;
                for (int dy = 0; dy < scale; dy++)
                    for (int dx = 0; dx < scale; dx++)
                        set_cell(cx + col * scale + dx, y + row * scale + dy, ' ', fg, bg);
            }
        }
    }
}

void Layer2D::draw_line(int x0, int y0, int x1, int y1, Vec3 color) {
    int dx = std::abs(x1 - x0), sx = x0 < x1 ? 1 : -1;
    int dy = -std::abs(y1 - y0), sy = y0 < y1 ? 1 : -1;
    int err = dx + dy;
    while (true) {
        set_cell(x0, y0, ' ', color, color);
        if (x0 == x1 && y0 == y1) break;
        int e2 = 2 * err;
        if (e2 >= dy) { err += dy; x0 += sx; }
        if (e2 <= dx) { err += dx; y0 += sy; }
    }
}

void Layer2D::composite_to(std::vector<Vec3>& target, int target_w, int target_h) const {
    if (!m_visible) return;
    int w = std::min(m_width, target_w), h = std::min(m_height, target_h);
    for (int y = 0; y < h; y++)
        for (int x = 0; x < w; x++) {
            const auto& c = m_cells[x + y * m_width];
            if (!c.transparent) target[x + y * target_w] = c.bg;
        }
}
